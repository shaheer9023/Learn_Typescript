"use strict";
// Intersection
let person = {
    name: "Shaheer ahmad",
    age: 21,
    email: "shaheerahmad9023@gmail.com",
    phone: 3127511011
};
console.log('===Complete Details===');
console.log(person); //this will print all the details
console.log('===Name===');
console.log(person.name); //this will print only name
console.log('===Age===');
console.log(person.age); //this will print only age
console.log('===Email===');
console.log(person.email); //this will print only email
console.log('===Phone===');
console.log(person.phone); //this will print only phone
