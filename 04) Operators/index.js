"use strict";
// operators
// 1. Arithmetic Operators
// +, -, *, /
console.log('arithmetic operators');
let x = 10;
console.log(x + 5); //15
console.log(x - 5); //5
console.log(x * 5); //50
console.log(x / 5); //2
// 2. Assignment Operators
// =, +=, -=, *=, /=, %=, **=
console.log('assignment operators');
let y = 20;
console.log(y = 10); //10
console.log(y += 1); //11
console.log(y -= 1); //10//value is liye 10 h q k code line by line read hota h
console.log(y *= 1); //10
console.log(y /= 1); //10
// 3. Comparison Operators
// ==, ===, !=, !==, >, <, >=, <=
console.log('comparison operators');
let z = 5;
console.log('5==5', z == 5); //true
console.log('5===5', z === 5); //true 
console.log('5!=5', z != 5); //false
console.log('5!==5', z !== 5); //false
console.log('5>7', z > 7); //false
console.log('5<7', z < 7); //true
console.log('5>=3', z >= 3); //true
console.log('5<=8', z <= 8); //true
